import re
pattern = re.compile(
    r"^" # beginning of the string
    r"(?!.*(\d)(-?\1){3})" # digits in groups of 4 separated by one hyphen "-"
    r"[456]" # must start with 4, 5 or 6
    r"\d{3}" # no 4 or more consecutive digits
    r"(?:-?\d{4}){3}"  # contain exactly 16 digits
    r"$") # end of the string
for _ in range(int(input())):
    print("Valid" if pattern.search(input()) else "Invalid")

