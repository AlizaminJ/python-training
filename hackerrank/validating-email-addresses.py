import re 

def fun(s):
    regex = '^[\\w-]+@[0-9a-zA-Z]+\\.[a-z]{1,3}$'
    if re.match(regex, s):
        return s

def filter_mail(emails):
    return list(filter(fun, emails))

if __name__ == '__main__':
    n = int(input())
    emails = []
    for _ in range(n):
        emails.append(input())

filtered_emails = filter_mail(emails)
filtered_emails.sort()
print(filtered_emails)